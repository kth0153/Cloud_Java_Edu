package j20220726;

public class If_3 {

	public static void main(String[] args) {
		//ctrl+shift+f 자동정렬
		int init = 30;

		if (init == 30) {
			System.out.println("30");
		}

		if (init == 40) {
			System.out.println("30");
		}

		if (init <= 30) {
			System.out.println("30보다 작다 ");
		}

	}
}
