package j20220726;

public class Opt_1 {
	
	
	public static void main(String[] args) {
		
		int init=0;
		
//		++변수:만나면 1증가 
//		변수++:만나고 또만나면 1증가 

		
		System.out.println(init);
		System.out.println(++init);
		System.out.println(init);
		System.out.println(++init);
		System.out.println(init++);
		System.out.println(init);
		System.out.println(init++);
		System.out.println(init++);
		System.out.println(++init);
		
		
		
		
		
	}
}
