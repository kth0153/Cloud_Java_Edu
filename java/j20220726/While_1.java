package j20220726;

public class While_1 {

	public static void main(String[] args) {
		int init=0;
			//1 ~ 10 합을구하시오 
		int sum=0;
		while (++init<=10) {
		sum=sum+init;
		}
		System.out.println(sum);
		
		
		//while 
		
//		0 ~ 21   짝수의합에서 4,6,10,20 을뺀합  홀수의합에서 7,11,19 을뺀합구하시오

		
		
		
		
		
	}
	
}
